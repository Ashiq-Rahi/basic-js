function name (firstName, lastName){
    var fullName = firstName + lastName;
    return fullName;
}

var yourName = name('Ashiq ', 'Al Alam');
console.log(yourName)

function id(name, roll, cat){
    var comb = name + roll + cat;
    return comb;
}

var yourId = id('Ashiq', 12, '2l');
console.log(yourId)